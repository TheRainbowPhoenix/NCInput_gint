static SDL_Rect clip_rect = {0, 0, SCREEN_W, SCREEN_H};

// --- Helper: Pixel Drawing ---
static void set_pixel(int x, int y, color_t c) {
    if (x >= clip_rect.x && x < clip_rect.x + clip_rect.w &&
        y >= clip_rect.y && y < clip_rect.y + clip_rect.h) {
        if (x >= 0 && x < SCREEN_W && y >= 0 && y < SCREEN_H) {
            vram[y * SCREEN_W + x] = c;
        }
    }
}

// --- Initialization ---
extern "C" void simulator_init() {
    if (SDL_Init(SDL_INIT_VIDEO | SDL_INIT_TIMER) < 0) return;
    window = SDL_CreateWindow("cinput Simulator", SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED, SCREEN_W, SCREEN_H, SDL_WINDOW_SHOWN);
    renderer = SDL_CreateRenderer(window, -1, SDL_RENDERER_ACCELERATED | SDL_RENDERER_PRESENTVSYNC);
    screen_texture = SDL_CreateTexture(renderer, SDL_PIXELFORMAT_RGB565, SDL_TEXTUREACCESS_STREAMING, SCREEN_W, SCREEN_H);
    memset(vram, 0xFF, sizeof(vram));
}

// --- Display API ---
void dclear(color_t color) {
    for (int i = 0; i < SCREEN_W * SCREEN_H; ++i) vram[i] = color;
}

void drect(int x1, int y1, int x2, int y2, color_t color) {
    int start_x = std::min(x1, x2);
    int end_x = std::max(x1, x2);
    int start_y = std::min(y1, y2);
    int end_y = std::max(y1, y2);
    for (int y = start_y; y < end_y; ++y) {
        for (int x = start_x; x < end_x; ++x) {
            set_pixel(x, y, color);
        }
    }
}

void drect_border(int x1, int y1, int x2, int y2, int fill_color, int width, int border_color) {
    if (fill_color != (int)C_NONE) drect(x1, y1, x2, y2, (color_t)fill_color);
    // Top
    drect(x1, y1, x2, y1 + width, (color_t)border_color);
    // Bottom
    drect(x1, y2 - width, x2, y2, (color_t)border_color);
    // Left
    drect(x1, y1, x1 + width, y2, (color_t)border_color);
    // Right
    drect(x2 - width, y1, x2, y2, (color_t)border_color);
}

void dline(int x1, int y1, int x2, int y2, color_t color) {
    int dx = abs(x2 - x1), sx = x1 < x2 ? 1 : -1;
    int dy = -abs(y2 - y1), sy = y1 < y2 ? 1 : -1;
    int err = dx + dy, e2;
    while (1) {
        set_pixel(x1, y1, color);
        if (x1 == x2 && y1 == y2) break;
        e2 = 2 * err;
        if (e2 >= dy) { err += dy; x1 += sx; }
        if (e2 <= dx) { err += dx; y1 += sy; }
    }
}

void dhline(int y, color_t color) {
    for (int x = 0; x < SCREEN_W; ++x) set_pixel(x, y, color);
}

void dvline(int x, color_t color) {
    for (int y = 0; y < SCREEN_H; ++y) set_pixel(x, y, color);
}

void dtext(int x, int y, color_t color, const char *text) {
    if (!text) return;
    while (*text) {
        uint8_t c = (uint8_t)*text;
        if (c >= 32 && c < 128) {
            const uint8_t* glyph = font8x8[c - 32];
            for (int gy = 0; gy < 8; gy++) {
                uint8_t row = glyph[gy];
                for (int gx = 0; gx < 8; gx++) {
                    if (row & (0x80 >> gx)) {
                        set_pixel(x + gx, y + gy, color);
                    }
                }
            }
        }
        x += 8;
        text++;
    }
}

void dtext_opt(int x, int y, color_t fg, int bg, int halign, int valign, const char *str, int size) {
    if (!str) return;
    int len = strlen(str);
    int tw = len * 8;
    int th = 8;

    int tx = x;
    if (halign == DTEXT_CENTER) tx = x - tw / 2;
    else if (halign == DTEXT_RIGHT) tx = x - tw;

    int ty = y;
    if (valign == DTEXT_MIDDLE) ty = y - th / 2;
    else if (valign == DTEXT_BOTTOM) ty = y - th;

    if (bg != (int)C_NONE) {
        drect(tx, ty, tx + tw, ty + th, (color_t)bg);
    }
    dtext(tx, ty, fg, str);
}

void dpoly(int const *px, int const *py, int n, int color, int border) {
    for (int i = 0; i < n; ++i) {
        int next = (i + 1) % n;
        dline(px[i], py[i], px[next], py[next], (color_t)color);
    }
}

void dsize(const char *str, void const *font, int *w, int *h) {
    if (!str) { if(w) *w = 0; if(h) *h = 0; return; }
    if(w) *w = strlen(str) * 8;
    if(h) *h = 8;
}

char const *drsize(const char *str, void const *font, int width, int *px) {
    if (!str) { if(px) *px = 0; return nullptr; }
    int max_chars = width / 8;
    int len = strlen(str);
    int count = std::min(max_chars, len);
    if(px) *px = count * 8;
    return str + count;
}

void dwindow_set(dwindow_t window) {
    clip_rect.x = std::min(window.x1, window.x2);
    clip_rect.y = std::min(window.y1, window.y2);
    clip_rect.w = std::abs(window.x2 - window.x1);
    clip_rect.h = std::abs(window.y2 - window.y1);
}

void dupdate(void) {
    SDL_UpdateTexture(screen_texture, nullptr, vram, SCREEN_W * sizeof(uint16_t));
    SDL_RenderClear(renderer);
    SDL_RenderCopy(renderer, screen_texture, nullptr, nullptr);
    SDL_RenderPresent(renderer);

    // Pump events
    SDL_Event e;
    while (SDL_PollEvent(&e)) {
        if (e.type == SDL_QUIT) exit(0);

        key_event_t kev = {KEYEV_NONE, 0, 0, 0};
        if (e.type == SDL_MOUSEBUTTONDOWN) {
            kev.type = KEYEV_TOUCH_DOWN;
            kev.x = e.button.x;
            kev.y = e.button.y;
            event_queue.push(kev);
        } else if (e.type == SDL_MOUSEBUTTONUP) {
            kev.type = KEYEV_TOUCH_UP;
            kev.x = e.button.x;
            kev.y = e.button.y;
            event_queue.push(kev);
        } else if (e.type == SDL_MOUSEMOTION && (e.motion.state & SDL_BUTTON_LMASK)) {
            kev.type = KEYEV_TOUCH_DRAG;
            kev.x = e.motion.x;
            kev.y = e.motion.y;
            event_queue.push(kev);
        } else if (e.type == SDL_KEYDOWN) {
            int key = 0;
            switch(e.key.keysym.sym) {
                case SDLK_UP: key = KEY_UP; break;
                case SDLK_DOWN: key = KEY_DOWN; break;
                case SDLK_LEFT: key = KEY_LEFT; break;
                case SDLK_RIGHT: key = KEY_RIGHT; break;
                case SDLK_RETURN: key = KEY_EXE; break;
                case SDLK_ESCAPE: key = KEY_EXIT; break;
                case SDLK_BACKSPACE: key = KEY_DEL; break;
                case SDLK_0: key = KEY_0; break;
                case SDLK_1: key = KEY_1; break;
                case SDLK_2: key = KEY_2; break;
                case SDLK_3: key = KEY_3; break;
                case SDLK_4: key = KEY_4; break;
                case SDLK_5: key = KEY_5; break;
                case SDLK_6: key = KEY_6; break;
                case SDLK_7: key = KEY_7; break;
                case SDLK_8: key = KEY_8; break;
                case SDLK_9: key = KEY_9; break;
            }
            if (key) {
                kev.type = KEYEV_DOWN;
                kev.key = key;
                event_queue.push(kev);
                pressed_keys[key] = true;
            }
        } else if (e.type == SDL_KEYUP) {
             int key = 0;
            switch(e.key.keysym.sym) {
                case SDLK_UP: key = KEY_UP; break;
                case SDLK_DOWN: key = KEY_DOWN; break;
                case SDLK_LEFT: key = KEY_LEFT; break;
                case SDLK_RIGHT: key = KEY_RIGHT; break;
                case SDLK_RETURN: key = KEY_EXE; break;
                case SDLK_ESCAPE: key = KEY_EXIT; break;
                case SDLK_BACKSPACE: key = KEY_DEL; break;
                case SDLK_0: key = KEY_0; break;
                case SDLK_1: key = KEY_1; break;
                case SDLK_2: key = KEY_2; break;
                case SDLK_3: key = KEY_3; break;
                case SDLK_4: key = KEY_4; break;
                case SDLK_5: key = KEY_5; break;
                case SDLK_6: key = KEY_6; break;
                case SDLK_7: key = KEY_7; break;
                case SDLK_8: key = KEY_8; break;
                case SDLK_9: key = KEY_9; break;
            }
            if (key) {
                pressed_keys[key] = false;
            }
        }
    }
}

// --- Keyboard API ---
void clearevents(void) {
    while(!event_queue.empty()) event_queue.pop();
}

void cleareventflips(void) {}

key_event_t pollevent(void) {
    if (event_queue.empty()) return {KEYEV_NONE, 0, 0, 0};
    key_event_t e = event_queue.front();
    event_queue.pop();
    return e;
}

int keypressed(int key) {
    return pressed_keys[key] ? 1 : 0;
}

int getkey(void) {
    while(true) {
        // We only dupdate() to keep the window responsive and draw latest VRAM,
        // but we don't want to re-pump events *inside* pollevent if dupdate also does it.
        // Wait, pollevent pops from the queue that dupdate fills.
        dupdate();
        if (!event_queue.empty()) {
            key_event_t e = event_queue.front();
            if (e.type == KEYEV_DOWN) {
                event_queue.pop();
                return e.key;
            }
            if (e.type == KEYEV_TOUCH_DOWN) {
                // Return something or ignore? Calculator getkey() usually waits for physical keys.
            }
        }
        SDL_Delay(10);
    }
}

// --- Timing API ---
uint32_t rtc_ticks(void) {
    return (uint32_t)(SDL_GetTicks() * 128 / 1000);
}

void sleep_ms(int ms) {
    // In sleep_ms, we should still pump events to keep window alive
    uint32_t start = SDL_GetTicks();
    while (SDL_GetTicks() - start < (uint32_t)ms) {
        dupdate();
        SDL_Delay(1);
    }
}
